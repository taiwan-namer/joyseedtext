// 右欄：加購項目
export type AddonPrice = { name: string; price: number };

// 所有課程詳情資料，slug 對應網址 /course/1, /course/2, ...
export type CourseDetail = {
  slug: string;
  category: string;
  title: string;
  ageRange: string;
  ageTags: string[];
  highlights: string[];
  articleParagraphs: string[];
  /** 課程簡介，約三行，顯示於 READ MORE 上方 */
  courseIntro?: string;
  /** 內文照片縮圖數量（主圖旁顯示） */
  thumbnailCount?: number;
  customerNotice?: CustomerNotice;
  /** 右欄選項標籤（對應後台多選：0-3歲、3-6歲、可大人陪同等） */
  sidebarOptionLabels?: string[];
  /** 可預約日期與時段（上架課程的場次） */
  scheduledSlots?: { date: string; time: string }[];
  /** 售價（原價） */
  price?: number;
  /** 特價（有則顯示，無則不顯示） */
  salePrice?: number | null;
  /** 加購價列表 */
  addonPrices?: AddonPrice[];
};

